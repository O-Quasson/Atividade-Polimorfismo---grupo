// 1.Elementos básicos das artes visuais
// Qual dos elementos abaixo NÃO é considerado um elemento básico das artes visuais?

// 1) Linha
// 2) Cor
// 3) Ritmo
// 4) Textura

// Resposta correta: 3) Ritmo
// Explicação:
// Nas artes visuais, os elementos básicos incluem linha, cor, forma, textura, espaço, luz/sombra e composição. Esses elementos são usados para construir e interpretar imagens.
// O ritmo, embora possa aparecer de forma metafórica (como repetição visual), é tecnicamente um elemento da música e da dança, relacionado à organização do tempo e do movimento. Portanto, não é considerado um elemento fundamental das artes visuais.



// 2.Técnicas de arte visual
// A técnica artística que utiliza tinta à base de óleo e permite grande riqueza de detalhes e profundidade é:

// 1) Aquarela
// 2) Pastel seco
// 3) Têmpera
// 4) Pintura a óleo

// Resposta correta: 4) Pintura a óleo
// Explicação:
// A pintura a óleo utiliza tintas feitas com pigmentos e óleo (geralmente óleo de linhaça), oferecendo grande durabilidade, profundidade de cor e capacidade de mistura e sobreposição.
// Essa técnica permite trabalhar detalhes minuciosos, texturas e efeitos de luz e sombra, o que a tornou muito popular entre os mestres renascentistas e barrocos.
// Outras técnicas, como aquarela ou pastel seco, também têm valor artístico, mas não oferecem o mesmo grau de profundidade e detalhamento que a pintura a óleo.



// 3.Arte contemporânea
// Uma das principais características da arte contemporânea é:

// 1) A valorização da simetria e do equilíbrio clássico.
// 2) O foco exclusivo em pintura e escultura.
// 3) A interdisciplinaridade e o uso de novas tecnologias e mídias.
// 4) O distanciamento do público e a negação de significados políticos.

// Resposta correta: 3) A interdisciplinaridade e o uso de novas tecnologias e mídias
// Explicação:
// A arte contemporânea (século XX em diante) rompe com os limites tradicionais das linguagens artísticas. Uma de suas principais características é a interdisciplinaridade — ou seja, o diálogo entre diferentes linguagens como vídeo, fotografia, instalação, performance, arte digital, entre outras.
// Além disso, a arte contemporânea muitas vezes incorpora novas tecnologias, trata de questões sociais, políticas e ambientais, e valoriza a interação com o público, desafiando a ideia de que a arte precisa ser apenas "bela" ou "decorativa".


